package exemplos;

public class Exemplo3 {
	public static void main(String[] args) {
		
		System.out.println(Math.pow(2, 3)); // Potenciação
		
		double r = Math.sqrt(16);
		System.out.println(r); // Raiz quadrada
		
		System.out.println(Math.ceil(3.1)); // Arredonda pra cima
		System.out.println(Math.floor(7.99)); //Arredonda pra baixo
		
		System.out.println(Math.round(4.4)); // Arredonda normalmente
		System.out.println(Math.round(4.6)); 
		
		System.out.println(Math.min(10, 20)); // Pega o maior valor
		System.out.println(Math.max(30, 40)); // Pega o menor
		
		System.out.println(Math.PI); // Valor do PI
	}
	
}
